def resolve_color(color: int | str) -> int:
    """Resolve a color name or integer to an RGB int.

    Args:
        color: 0xRRGGBB integer or a named color string.

    Returns:
        Color as 0xRRGGBB integer.

    Raises:
        ValueError: If the color name is unknown.
        TypeError: If color is not an int or str.
    """
    if isinstance(color, int):
        return color
    if not isinstance(color, str):
        raise TypeError("Color must be an int or color name string")

    key = color.strip().lower().replace(" ", "").replace("-", "")
    if key in CSS_COLORS:
        return CSS_COLORS[key]
    raise ValueError(f"Unknown color name: {color}")
